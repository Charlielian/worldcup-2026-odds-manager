def is_national_team(team_name: str) -> bool:
    return team_name in NATIONAL_TEAMS


def get_team_icon(team_name: str) -> dict:
    if is_national_team(team_name):
        return {
            'type': 'national',
            'flag': FLAG_MAP.get(team_name, ''),
            'logo_url': '',
            'color': '',
        }
    color = CLUB_COLORS.get(team_name, '#4A90D9')
    text = team_name[:2] if team_name else '??'
    encoded = urllib.parse.quote(text)
    logo_url = (
        f"https://ui-avatars.com/api/?name={encoded}"
        f"&background={color.replace('#', '')}&color=fff&size=80&bold=true&format=svg"
    )
    return {
        'type': 'club',
        'flag': '',
        'logo_url': logo_url,
        'color': color,
    }


def get_flag(team_name: str) -> str:
    if is_national_team(team_name):
        return FLAG_MAP.get(team_name, '')
    return ''


def get_flag_map_dict() -> dict:
    return dict(FLAG_MAP)
