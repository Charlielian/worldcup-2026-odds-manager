def main():
    if len(SCHEDULE) != 72:
        print(f"警告：赛程条目数 = {len(SCHEDULE)}，应为 72", file=sys.stderr)

    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()

    updated = 0
    not_found = []
    multi_found = []

    for team1, team2, group, new_time in SCHEDULE:
        cursor.execute(
            "SELECT id, match_time FROM matches "
            "WHERE team1=? AND team2=? AND group_name=? AND stage='小组赛'",
            (team1, team2, group),
        )
        rows = cursor.fetchall()
        if not rows:
            not_found.append((team1, team2, group))
            continue
        if len(rows) > 1:
            multi_found.append((team1, team2, group, [r[0] for r in rows]))
            continue

        match_id, old_time = rows[0]
        cursor.execute(
            "UPDATE matches SET match_time=? WHERE id=?",
            (new_time, match_id),
        )
        if old_time != new_time:
            print(f"[更新] id={match_id} {old_time} -> {new_time}  {team1} VS {team2}")
        else:
            print(f"[一致] id={match_id} {new_time}  {team1} VS {team2}")
        updated += 1

    conn.commit()
    conn.close()

    print(f"\n=== 完成 ===")
    print(f"处理: {updated} 场")
    if not_found:
        print(f"未找到: {len(not_found)} 场")
        for t1, t2, g in not_found:
            print(f"  - {g} {t1} VS {t2}")
    if multi_found:
        print(f"多条匹配: {len(multi_found)} 场，需人工确认")
        for t1, t2, g, ids in multi_found:
            print(f"  - {g} {t1} VS {t2} -> ids={ids}")

    return 0 if not not_found and not multi_found else 1


if __name__ == "__main__":
    sys.exit(main())
