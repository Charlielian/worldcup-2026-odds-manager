def update_matches():
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()
    
    # 删除所有现有比赛
    print("删除现有比赛数据...")
    cursor.execute("DELETE FROM matches")
    
    # 按时间顺序插入新赛程
    match_id = 1
    for match_time in sorted(FIFA_SCHEDULE.keys()):
        for team1, team2, stage in FIFA_SCHEDULE[match_time]:
            # 判断是小组赛还是淘汰赛
            if '组' in stage and '第' in stage:
                # 小组赛，提取组别
                group = stage[0] + '组' if len(stage) >= 2 else ''
                stage_type = '小组赛'
            else:
                group = None
                stage_type = stage
            
            cursor.execute("""
                INSERT INTO matches (team1, team2, match_time, stage, group_name, status)
                VALUES (?, ?, ?, ?, ?, ?)
            """, (team1, team2, match_time, stage_type, group, 'upcoming'))
            print(f"插入: {match_time} {team1} vs {team2} ({stage})")
            match_id += 1
    
    conn.commit()
    
    # 验证插入数量
    cursor.execute("SELECT COUNT(*) FROM matches")
    count = cursor.fetchone()[0]
    print(f"\n总共插入 {count} 场比赛")
    
    conn.close()

if __name__ == '__main__':
    update_matches()
